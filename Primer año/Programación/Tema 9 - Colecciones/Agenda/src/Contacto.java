public class Contacto {

    private String nombre;
    private String tlf;

    public Contacto(String nombre, String tlf) {
        this.nombre = nombre;
        this.tlf = tlf;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getTlf() {
        return tlf;
    }

    public void setTlf(String tlf) {
        this.tlf = tlf;
    }
}
